    <h2 class="head-lines">So, What is a Code Snippet?</h2>
    <br />
    <p class="info">
        "Code Snippet" is a term used to describe a small portion of re-usable
        source code, machine code, or text. They allow a programmer to avoid
        typing repetitive code during the course of routine programming. Most
        modern text editing programs possess some form of Snippet creation and
        management (I say most because I do not know of every single editor that
        exists). While the exact way you manage Code Snippets does vary slightly
        from editor to editor, the basic concept is the same. For examples I
        will be using Visual Studio Code.
    </p>
    <br />
    <h2 class="head-lines">What Comprises a Code Snippet?</h2>
    <br />
    <p class="info">
        1. Name - Pretty self explanatory. A name for the snippet. This is not
        what is used to trigger the code snippet in the editor 2.Prefix - The
        actual keystroke that will be used to trigger the Code Snippet in the
        code editor. Choose a prefix that is easy for you to remember. 3. Body -
        The actual code that will be copied into the space when you call your
        code snippet. 4. Description - A simple description of what the code
        snippet contains.
    </p>
    <h2 class="head-lines">
        Well, why should I even bother using Code Snippets?
    </h2>
    <p>
        1. They save us from unnecessary amounts of keystrokes (your wrists will
        thank you). Remember, we should be as lazy as humanly possible whenever
        we can. 2.They make it easier to remember specific formatting for
        functions. Some functions can be extremely complex or may contain
        difficult to remember syntax. Instead of having to hunt down typos and
        syntax errors, let a Code Snippet keep your code consistent. 3.They are
        sharable (Don't be stingy with your Snippets). If you are on a team
        working on a project, a Code Snippet can help ensure that you are all
        writing certain blocks of code in the same way - making your code more
        consistent and more readable.
    </p>
